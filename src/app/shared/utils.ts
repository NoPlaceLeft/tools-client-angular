export const AlphaNumericPattern = /^[a-zA-Z0-9]+$/;
export const PasswordPattern = /^(?=.{6,}$)(?=.*?[a-z])(?=.*?[A-Z])(?=.*?[0-9])(?=.*[!@#$%^&*\(\)-+?<>,.;:'"]).*$/;
export const LowerCasePattern = /^(?=.{6,}$)(?=.*?[a-z]).*$/;
export const UpperCasePattern = /^(?=.{6,}$)(?=.*?[A-Z]).*$/;
export const NumberPattern = /^(?=.*?[0-9]).*$/;
export const SpecialCharacterPattern = /^(?=.*?[!@#$%^&*\(\)-+?<>,.;:'"]).*$/;
